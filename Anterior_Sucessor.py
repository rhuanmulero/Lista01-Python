#1.	Receba via teclado um número qualquer e exibir o seu sucessor e seu antecessor. 

print("Bem-vindo ao AES(Anterior e Sucessor)")

numero = int(input("Digite Um Número Inteiro Qualquer: "))

antecessor = numero - 1
sucessor = numero + 1

print("o Antecessor do seu Número é: ", antecessor)
print("Seu Número é: ", numero)
print("o Sucessor do seu Número é: ", sucessor)
